        /**
         * Type into a contenteditable element
         */
        async typeIntoContentEditable(element, text) {
            element.focus();
            
            // Clear existing content
            element.textContent = '';
            element.innerHTML = '<p class="x1oj8htv x2dq9o6 xxzylry x1mfz1tq xdj266r x14z9mp xat24cr x1lziwak xv54qhq" dir="auto"><br></p>';
            
            // Set text content
            const p = element.querySelector('p') || element;
            p.textContent = text;
            
            // Dispatch events to trigger React/Facebook's event handlers
            element.dispatchEvent(new InputEvent('input', { 
                bubbles: true, 
                cancelable: true,
                inputType: 'insertText',
                data: text
            }));
            
            element.dispatchEvent(new Event('change', { bubbles: true }));
            
            // Also try composition events for better compatibility (if available)
            if (typeof CompositionEvent !== 'undefined') {
                try {
                    element.dispatchEvent(new CompositionEvent('compositionstart', { bubbles: true }));
                    element.dispatchEvent(new CompositionEvent('compositionupdate', { bubbles: true, data: text }));
                    element.dispatchEvent(new CompositionEvent('compositionend', { bubbles: true, data: text }));
                } catch (e) {
                    Logger.debug('CompositionEvent not supported, skipping');
                }
            }
            
            Logger.debug(`⌨️ Typed: "${text.substring(0, 50)}..."`);
            
            // Small delay to ensure the text is set
            await this.delay(300);
        }

        /**
         * Click send button
         */
        async clickSendButton(retries = 10) {
            for (let attempt = 0; attempt < retries; attempt++) {
                // Try each selector
                for (const selector of MetaAIMediaEnhancer.SELECTORS.SEND_BUTTON) {
                    const button = document.querySelector(selector);
                    if (button && button.offsetParent !== null) {
                        // Check if button is disabled
                        const isDisabled = button.getAttribute('aria-disabled') === 'true';
                        
                        if (!isDisabled) {
                            // Use MouseEventUtils for better compatibility
                            const clickEvent = MouseEventUtils.createClickEvent({ 
                                bubbles: true, 
                                cancelable: true, 
                                programmatic: true 
                            });
                            button.dispatchEvent(clickEvent);
                            Logger.debug(`📤 Clicked Send button using selector: ${selector}`);
                            // Small delay to allow DOM to update after click
                            await this.delay(200);
                            return;
                        }
                    }
                }
                
                Logger.debug(`⏱ Waiting for Send button to be ready... (attempt ${attempt + 1}/${retries})`);
                await this.delay(500);
            }
            
            // Log available buttons for debugging
            const allSendButtons = document.querySelectorAll('div[aria-label="Send"]');
            Logger.error(`❌ Send button not found or disabled. Found ${allSendButtons.length} button(s) with aria-label="Send":`, 
                Array.from(allSendButtons).map(btn => ({
                    ariaDisabled: btn.getAttribute('aria-disabled'),
                    visible: btn.offsetParent !== null,
                    classes: btn.className
                }))
            );
            
            throw new Error("❌ Send button not found or disabled");
        }

        /**
         * Wait for image generation to complete
         */
        async waitForCompletion(timeout = null) {
            const timeoutMs = timeout || this.settings.COMPLETION_TIMEOUT || 60000;
            const start = Date.now();
            
            Logger.debug("🕐 Waiting for image generation to complete...");
            
            // Wait for send button to become disabled (request sent)
            let disabledCheckCount = 0;
            while (true) {
                const sendButton = document.querySelector(MetaAIMediaEnhancer.SELECTORS.SEND_BUTTON_DISABLED[0]);
                if (sendButton) {
                    Logger.debug("✅ Request sent, waiting for completion...");
                    break;
                }
                
                if (Date.now() - start > 5000) {
                    Logger.warn('Send button did not become disabled, continuing anyway');
                    break;
                }
                
                disabledCheckCount++;
                if (disabledCheckCount % 10 === 0) {
                    Logger.debug('Still waiting for send button to become disabled...');
                }
                await this.delay(200);
            }
            
            // Wait for send button to become enabled again (generation complete)
            let enabledCheckCount = 0;
            while (true) {
                if (Date.now() - start > timeoutMs) {
                    Logger.warn('Timeout waiting for completion, continuing to next prompt');
                    break;
                }
                
                if (this.shouldStop) {
                    throw new Error("Automation stopped by user");
                }
                
                // Check if send button is enabled again
                const sendButton = document.querySelector(MetaAIMediaEnhancer.SELECTORS.SEND_BUTTON[0]);
                if (sendButton && sendButton.offsetParent !== null) {
                    const isDisabled = sendButton.getAttribute('aria-disabled') === 'true';
                    if (!isDisabled) {
                        Logger.debug("✅ Image generation completed");
                        // Additional delay to ensure image is fully loaded
                        await this.delay(1000);
                        return;
                    }
                }
                
                enabledCheckCount++;
                if (enabledCheckCount % 10 === 0) {
                    Logger.debug('Still waiting for completion...');
                }
                await this.delay(500);
            }
        }

